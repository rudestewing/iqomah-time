    {
        $request->validate([
            'image' => ['nullable', 'mimes:jpg,jpeg,png', 'max:2000'],
        ]);

        if($request->image) {
            $data = SettingBackground::query()->first()->update([
                'image_path' => $request->file('image')->store('/', 'public')
            ]);
        }

        return redirect()->back()->with('success', '');
